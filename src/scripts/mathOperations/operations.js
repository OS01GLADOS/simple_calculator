// division
function division(a, b){
    return a/b
}

// multiplication
function multiplication(a, b){
    return a*b
}

// substraction
function substraction(a, b){
    return a - b 
}

// addition
function addition(a, b){
    return a +b
}

// percent
function percent(a, b=1){
    return b* (a/100)
}

export { multiplication, division, substraction, addition, percent};